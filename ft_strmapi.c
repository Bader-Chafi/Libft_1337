#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	int		i;
	int		len;
	char	*string;

	i = 0;
	if (!s || !f)
		return (NULL);
	len = ft_strlen(s);
	string = (char *)malloc(len + 1);
	if (!string)
		return (NULL);
	while (s[i])
	{
		string[i] = f(i, s[i]);
		i++;
	}
	string[i] = '\0';
	return (string);
}
